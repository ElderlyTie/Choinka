int przerwa =500;

void setup() {
    for(int i = 0; i<15; i++){
        pinMode(piny[i], OUTPUT);
        ledcAttachPin(piny[i], i);
        ledcSetup(i, 5000, 8);
    }
}
void loop() {
    for(int j = 0; j<3; j++){
        for(int i = 0; i<15; i++){
            ledcWrite(i, rand()%(255));
        }
        delay(przerwa);
    }
    
}
